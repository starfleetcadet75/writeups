class CPU:
    def __init__(self, flag):
        # Mimics the setup function from the actual program
        self.code = BYTECODE
        self.stack = bytearray([0] * 0x800)
        self.pc = 0
        self.sp = 0

        # Write the user input into memory at the expected location
        flag += "\n"
        for i in range(len(flag)):
            self.code[0x400 + i] = ord(flag[i])

    def run(self):
        while True:
            opcode = self.code[self.pc]
            # print("[PC: {:04x} | SP: {:04x} | Opcode: {:04x}]".format(self.pc, self.sp, opcode))

            if opcode == 0x43:  # add
                op1 = (self.stack[self.sp + 1] << 8) | self.stack[self.sp]
                op2 = (self.stack[self.sp - 1] << 8) | self.stack[self.sp - 2]
                self.sp -= 2

                result = (op1 + op2)
                print("add {:04x}, {:04x} = {:04x}".format(op1, op2, result))

                self.stack[self.sp] = result & 0xff
                self.stack[self.sp + 1] = (result >> 8) & 0xff
                self.pc += 1

            elif opcode == 0x51:  # sub
                op2 = (self.stack[self.sp + 1] << 8) | self.stack[self.sp]
                op1 = (self.stack[self.sp - 1] << 8) | self.stack[self.sp - 2]
                self.sp -= 2

                result = op1 - op2
                print("sub {:04x}, {:04x} = {:04x}".format(op1, op2, result))

                self.stack[self.sp] = result & 0xff
                self.stack[self.sp + 1] = (result >> 8)
                self.pc += 1

            elif opcode == 0xf0:  # xor
                op1 = (self.stack[self.sp + 1] << 8) | self.stack[self.sp]
                op2 = (self.stack[self.sp - 1] << 8) | self.stack[self.sp - 2]
                self.sp -= 2

                result = op1 ^ op2
                print("xor {:04x}, {:04x} = {:04x}".format(op1, op2, result))

                self.stack[self.sp] = result & 0xff
                self.stack[self.sp + 1] = (result >> 8)
                self.pc += 1

            elif opcode == 0x12:  # copy?
                op1 = self.stack[self.sp + 1]
                op2 = self.stack[self.sp]
                self.sp += 2
                print("copy? {:04x}".format((op1 << 8) | op2))
                self.stack[self.sp + 1] = op1
                self.stack[self.sp] = op2
                self.pc += 1

            elif opcode == 0x38:  # swap
                op = (self.stack[self.sp + 1] << 8) | self.stack[self.sp]

                op1 = self.stack[self.sp + 1]
                op2 = self.stack[self.sp]
                print("swap {:04x}".format((op1 << 8) | op2))

                self.stack[self.sp + 1] = self.stack[self.sp - 3]
                self.stack[self.sp] = self.stack[self.sp - 2]
                self.stack[self.sp - 1] = op1
                self.stack[self.sp - 2] = op2
                self.pc += 1

            elif opcode == 0x93:  # pop
                self.sp += 2
                op1 = self.code[self.pc + 2]
                op2 = self.code[self.pc + 1]
                print("pop {:04x}".format((op1 << 8) | op2))
                self.stack[self.sp + 1] = op1
                self.stack[self.sp] = op2
                self.pc += 3

            elif opcode == 0x02:  # load
                op = (self.stack[self.sp + 1] << 8) | self.stack[self.sp]
                print("ld [{:04x}]".format(op + op))
                self.stack[self.sp] = self.code[op + op]
                self.stack[self.sp + 1] = self.code[op + op + 1]
                self.pc += 1

            elif opcode == 0xc1:  # store
                op1 = (self.stack[self.sp + 1] << 8) | self.stack[self.sp]
                op2 = (self.stack[self.sp - 1] << 8) | self.stack[self.sp - 2]
                self.sp -= 4
                print("store [{:04x}], {:04x}".format(op2 + op2, op1))
                self.code[op2 + op2] = op1 & 0xff
                self.code[op2 + op2 + 1] = op1 >> 8
                self.pc += 1

            elif opcode == 0xa4:  # cmp
                op1 = (self.stack[self.sp + 1] << 8) | self.stack[self.sp]
                op2 = (self.stack[self.sp - 1] << 8) | self.stack[self.sp - 2]
                self.sp -= 2
                result = int(op1 == op2)
                print("cmp {:04x}, {:04x} = {:04x}".format(op1, op2, bool(result)))
                self.stack[self.sp] = result & 0xff
                self.stack[self.sp + 1] = (result >> 8)
                self.pc += 1

            elif opcode == 0x22:  # sal?
                op = (self.stack[self.sp + 1] << 8) | self.stack[self.sp]
                result = (op & 0xff00) | (op << 4) & 0xf0 | (op >> 4) & 0xf
                print("unk_22 {:04x} = {:04x}".format(op, result))
                self.stack[self.sp] = result & 0xff
                self.stack[self.sp + 1] = (result >> 8)
                self.pc += 1

            elif opcode == 0xd7:  # enc2
                op = (self.stack[self.sp + 1] << 8) | self.stack[self.sp]
                result = (op >> 4) & 0xf00 | (op << 4) & 0xf000 | op & 0xff
                print("enc2 {:04x} = {:04x}".format(op, result))
                self.stack[self.sp] = result & 0xff
                self.stack[self.sp + 1] = (result >> 8)
                self.pc += 1

